package Exercio04;

public class FaixaSalarialMensal {
	private int idSalario;
	private String faixaSalarial;

	public FaixaSalarialMensal() {
		super();
		this.idSalario = idSalario;
		this.faixaSalarial = faixaSalarial;
	}

	public int getIdSalario() {
		return idSalario;
	}

	public void setIdSalario(int idSalario) {
		this.idSalario = idSalario;
	}

	public String getFaixaSalarial() {
		return faixaSalarial;
	}

	public void setFaixaSalarial(String faixaSalarial) {
		this.faixaSalarial = faixaSalarial;
	}

}
